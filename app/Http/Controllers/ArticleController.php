     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'title' => 'required|max:255',
            'url' => 'required',
            'description' => 'required',
            'content' => 'required',
        ]);

        $request['user_id'] = Auth::user()->id;

        Article::create($request->all());
